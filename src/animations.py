import abc


class Animation(abc.ABC):
    DELAY = 0.2
    ANIMATION_TAPE = "."

    def __getitem__(self, i: int) -> str:
        return f'```{self.ANIMATION_TAPE[i]}```'

    def __len__(self) -> int:
        return len(self.ANIMATION_TAPE)

